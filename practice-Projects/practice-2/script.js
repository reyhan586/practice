
let star = '*'; 
console.log(star);

for(let i = 0; i < 5 ; i++){
    if( i === 0){
        console.log(star + '*');
    }
    else if ( i === 1){
        console.log(star + '**');
    }
    else if ( i === 2){
        console.log(star + '***');
    }
    else if ( i === 3){
        console.log(star + '****');
    }
}